package mathOperations;

public class MathOperations {
    public static void main(String[] args) {
        int x = 125;
        int y = 24;
        int z = x + y;
        int z1 = x *y;
        int z2 = x/y;
        int z3 = x-y;
        int z4 = x % y;
        System.out.println("print the sum " + z);
        System.out.println("print the mul " + z1);
        System.out.println("print the div " + z2);
        System.out.println("print the min " + z3);
        System.out.println("print the mod " + z4);
    }

}